package Aula2;

public class ParÍmpar {
    public static void main(String[] args) {
        int numero = 223;

        if(numero % 2 == 0) {
            System.out.println("O numero " + numero + " é par");
        } else {
            System.out.println("O numero " + numero + " é ímpar");
        }
    }
}
